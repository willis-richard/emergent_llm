        if num_past_rounds >= 3:
            decay_weights[-3:] *= 2.0
        total_weight = np.sum(decay_weights)
        if total_weight > 0:
            weighted_avg = np.sum(coop_rates * decay_weights) / total_weight
        else:
            weighted_avg = 0.5
        return weighted_avg

