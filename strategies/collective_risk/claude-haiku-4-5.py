class Strategy_INDIVIDUALISTIC_3(BaseStrategy):
    """
    Adaptive Threshold-Sensitive Strategy for Collective Risk Dilemma.
    
    Maintains an empirical estimate of opponent cooperation rate and cooperates
    when the probability of being the marginal cooperator (pushing the group over
    threshold m) exceeds the cost-benefit ratio 1/k. Defects in round 1 for safe
    exploration, then applies threshold rule based on binomial probability.
    """

    def __init__(self, game_description: CollectiveRiskDescription):
        self.game_description = game_description

    def __call__(self, history: PlayerHistory) -> Action:
        if history.round_number == 0:
            return Action.D
        total_cooperators_observed = int(np.sum(history.opponent_cooperators))
        total_opponent_actions = history.round_number * (self.game_description.n_players - 1)
        if total_opponent_actions == 0:
            return Action.D
        p_hat = total_cooperators_observed / total_opponent_actions
        n = self.game_description.n_players - 1
        k_binom = self.game_description.m - 1
        if k_binom < 0 or k_binom > n:
            prob = 0.0
        else:
            comb_val = math.comb(n, k_binom)
            prob = comb_val * p_hat ** k_binom * (1 - p_hat) ** (n - k_binom)
        threshold = 1.0 / self.game_description.k
        if prob > threshold:
            return Action.C
        else:
            return Action.D

class Strategy_INDIVIDUALISTIC_4(BaseStrategy):
    """
    Individualistic Adaptive Threshold Strategy for Collective Risk Dilemma.
    
    Tracks opponent cooperation rates and makes decisions based on whether
    the threshold is likely to be reached with/without the player's contribution.
    Defects in round 1 and the last round to avoid sucker payoffs and exploit
    final-round advantages. In intermediate rounds, estimates expected opponent
    cooperators and cooperates only when necessary to reach the threshold.
    """

    def __init__(self, game_description: CollectiveRiskDescription):
        self.game_description = game_description

    def __call__(self, history: PlayerHistory) -> Action:
        n = self.game_description.n_players
        r = self.game_description.n_rounds
        m = self.game_description.m
        if history.round_number == 0:
            return Action.D
        if history.round_number == r - 1:
            return Action.D
        total_opponent_cooperators = np.sum(history.opponent_cooperators)
        total_opponent_rounds = history.round_number * (n - 1)
        if total_opponent_rounds == 0:
            return Action.D
        p = total_opponent_cooperators / total_opponent_rounds
        E = (n - 1) * p
        if E < m - 1:
            return Action.D
        elif m - 1 <= E <= m:
            return Action.C
        else:
            return Action.D