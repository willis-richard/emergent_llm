class Strategy_INDIVIDUALISTIC_1(BaseStrategy):
    """
    Empirical Threshold Cooperator strategy for Collective Risk Dilemma.
    
    Cooperates based on observed cooperation rates among opponents, comparing
    against a critical threshold needed to achieve the collective payoff.
    Always defects in the final round. Uses initial heuristic for first round.
    """

    def __init__(self, game_description: CollectiveRiskDescription):
        self.game_description = game_description
        self.n = game_description.n_players
        self.r = game_description.n_rounds
        self.m = game_description.m

    def __call__(self, history: PlayerHistory) -> Action:
        current_round = history.round_number
        if current_round == self.r - 1:
            return Action.D
        if current_round == 0:
            if self.m <= self.n / 2 + 1:
                return Action.C
            else:
                return Action.D
        p_critical = (self.m - 1) / (self.n - 1)
        c_past = np.sum(history.opponent_cooperators[:current_round])
        n_past = (self.n - 1) * current_round
        if n_past == 0:
            p_empirical = 0.0
        else:
            p_empirical = c_past / n_past
        if p_empirical >= p_critical:
            return Action.C
        else:
            return Action.D

