        if round_num == r - 1:
            if expected_coop >= m:
                return Action.D
            elif expected_coop + 1 >= m:
                return Action.C
            else:
                return Action.D
        elif expected_coop >= m - 1:
            return Action.D
        elif expected_coop + 1 >= m:
            return Action.C
        else:
            return Action.D

