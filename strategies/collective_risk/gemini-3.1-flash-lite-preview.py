            recent_opponents = history.opponent_cooperators[-3:]
            recent_my_actions = history.my_actions[-3:]
            fail_count = 0
            for i in range(3):
                my_c = 1 if recent_my_actions[i] else 0
                if recent_opponents[i] + my_c < m:
                    fail_count += 1
