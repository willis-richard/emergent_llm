class Strategy_ALTRUISTIC_1(BaseStrategy):
    """
    A strategy that employs Conditional Threshold Reciprocity.
    It cooperates initially, then adapts based on historical participation rates and 
    the need to meet the threshold 'm', while incorporating safeguards against 
    exploitation and coordination failure.
    """

    def __init__(self, game_description: CollectiveRiskDescription):
        self.game_description = game_description
        self.n = game_description.n_players
        self.m = game_description.m
        self.r = game_description.n_rounds
        self.target_ratio = self.m / self.n
        self.last_action_was_c = False

    def __call__(self, history: PlayerHistory) -> Action:
        t_zero_indexed = history.round_number
        t = t_zero_indexed + 1
        if t_zero_indexed == 0:
            self.last_action_was_c = True
            return Action.C
        prev_my_action_int = 1 if self.last_action_was_c else 0
        prev_round_idx = t_zero_indexed - 1
        prev_nc = history.opponent_cooperators[prev_round_idx] + prev_my_action_int
        all_prev_my_actions = history.my_actions.astype(int)
        all_prev_nc = history.opponent_cooperators + all_prev_my_actions
        cumulative_nc = np.sum(all_prev_nc)
        T = t_zero_indexed
        avg_p = cumulative_nc / (T * self.n)
        decision = Action.C
        if t == self.r:
            if self.last_action_was_c:
                decision = Action.C
            elif avg_p > 0.5 * self.target_ratio:
                decision = Action.C
            else:
                decision = Action.D
        else:
            is_declining = prev_nc < self.m
            if prev_nc == self.m:
                decision = Action.C
            elif is_declining:
                prev_observed_others = history.opponent_cooperators[prev_round_idx]
                ntr = self.m - prev_observed_others
                if ntr <= 1:
                    decision = Action.C
                else:
                    decision = Action.D
            elif avg_p >= self.target_ratio:
                decision = Action.C
            else:
                decision = Action.C
        self.last_action_was_c = decision == Action.C
        return decision

