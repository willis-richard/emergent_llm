class Strategy_SELFINTERESTED_1(BaseStrategy):
    """
    Adaptive strategy for the Collective Risk Dilemma.

    Core idea:
    - Cooperate in round 1.
    - Use recent cooperation levels, short streaks, and a small amount of
      conservative exploration to decide later rounds.
    - Prefer stability on strong success/failure streaks, but fall back to
      last-observation and majority-history rules when history is short.
    """

    def __init__(self, game_description: CollectiveRiskDescription):
        self.game_description = game_description
        self.n = int(game_description.n_players)
        self.r = int(game_description.n_rounds)
        self.m = int(game_description.m)
        self.k = float(game_description.k)
        self.n_opponents = max(0, self.n - 1)
        self.success_threshold = max(0, self.m - 1)
        self.rounds_played = 0

    def _successes(self, history: PlayerHistory):
        thresh = self.success_threshold
        return [int(x) >= thresh for x in history.opponent_cooperators]

    def _all_true(self, seq):
        return len(seq) > 0 and all(seq)

    def _all_false(self, seq):
        return len(seq) > 0 and (not any(seq))

    def _majority_success(self, seq):
        return sum((1 for v in seq if v)) >= math.ceil(len(seq) / 2)

    def _recent_window_stats(self, history: PlayerHistory, window: int):
        if history.round_number <= 0:
            return (0.0, 0, 0)
        start = max(0, history.round_number - window)
        vals = history.opponent_cooperators[start:history.round_number]
        if len(vals) == 0:
            return (0.0, 0, 0)
        q_vals = []
        for x in vals:
            if self.n_opponents > 0:
                q_vals.append(float(x) / float(self.n_opponents))
            else:
                q_vals.append(0.0)
        S = float(np.mean(np.array(q_vals, dtype=float))) if len(q_vals) > 0 else 0.0
        H = sum((1 for x in vals if int(x) >= self.success_threshold))
        return (S, H, len(vals))

    def __call__(self, history: PlayerHistory) -> Action:
        self.rounds_played = history.round_number
        if history.round_number == 0:
            return Action.C
        successes = self._successes(history)
        last_success = successes[-1]
        last3 = successes[-3:] if len(successes) >= 3 else successes[:]
        last4 = successes[-4:] if len(successes) >= 4 else successes[:]
        all_last3_success = self._all_true(last3) and len(last3) == 3
        all_last3_fail = self._all_false(last3) and len(last3) == 3
        exploratory_recovery = len(successes) >= 4 and self._all_false(successes[-4:-1]) and successes[-1]
        if self.r == 2:
            return Action.C if last_success else Action.D
        if all_last3_success:
            return Action.C
        if all_last3_fail:
            return Action.D
        if exploratory_recovery:
            return Action.C
        unanimous_opponents_last_round = history.opponent_cooperators[-1] == self.n_opponents if self.n_opponents > 0 else True
        if self.n == self.m + 1:
            return Action.C if unanimous_opponents_last_round else Action.D
        if self.m == self.n - 1:
            recent = successes[-3:] if len(successes) >= 3 else successes[:]
            if len(recent) >= 2 and sum((1 for v in recent if v)) >= 2:
                return Action.C
            return Action.D
        if history.round_number == self.r - 1:
            return Action.C if last_success else Action.D
        if self.r <= 4:
            recent = successes[-min(3, len(successes)):]
            if last_success or self._majority_success(recent):
                return Action.C
            return Action.D
        window = min(5, history.round_number)
        S_t, H_t, L = self._recent_window_stats(history, window)
        base_threshold = (self.m - 1) / float(self.n - 1) if self.n > 1 else 1.0
        majority_needed = math.ceil(L / 2) if L > 0 else 1
        penalty = 0
        if history.round_number >= 1:
            my_last_action = bool(history.my_actions[-1])
            if last_success and (not my_last_action):
                penalty = 1
            elif not last_success and my_last_action:
                recent_failures = sum((1 for v in successes[-5:] if not v))
                if recent_failures > 1:
                    return Action.D
        required_H = majority_needed + penalty
        if required_H > L:
            required_H = L
        if S_t >= base_threshold and H_t >= required_H:
            return Action.C
        if len(successes) >= 3:
            tri = successes[-3:]
            if any(tri) and (not all(tri)):
                if history.round_number % 4 == 0:
                    return Action.C
        return Action.D

