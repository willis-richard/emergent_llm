class Strategy_OPPORTUNISTIC_3(BaseStrategy):
    """
    Threshold-aware conditional cooperator with short-memory recovery.

    - Round 1: defect.
    - Main rule: defect when the previous round already met the threshold,
      cooperate when exactly one opponent short, and otherwise cooperate only
      if recent average opponent cooperation is sufficiently high.
    - Recovery rule: after two consecutive rounds below m - 1 opponent
      cooperators, cooperate once in the next round, then resume the main rule.
    """

    def __init__(self, game_description: CollectiveRiskDescription):
        self.game_description = game_description
        self._recovery_next = False
        self._last_seen_round = -1

    def __call__(self, history: PlayerHistory) -> Action:
        m = self.game_description.m
        round_number = history.round_number
        if round_number == self._last_seen_round:
            if self._recovery_next:
                return Action.C
            if round_number == 0:
                return Action.D
        if round_number == 0:
            self._last_seen_round = 0
            self._recovery_next = False
            return Action.D
        if self._recovery_next:
            self._last_seen_round = round_number
            self._recovery_next = False
            return Action.C
        x_prev = int(history.opponent_cooperators[-1])
        window_start = max(0, round_number - 3)
        recent = history.opponent_cooperators[window_start:round_number]
        mu_prev = float(np.mean(recent)) if len(recent) > 0 else 0.0
        if round_number >= 2:
            if int(history.opponent_cooperators[-1]) < m - 1 and int(history.opponent_cooperators[-2]) < m - 1:
                self._recovery_next = True
        if x_prev >= m:
            action = Action.D
        elif x_prev == m - 1:
            action = Action.C
        else:
            action = Action.C if mu_prev >= m - 1 else Action.D
        self._last_seen_round = round_number
        return action

