class Strategy_INDIVIDUALISTIC_3(BaseStrategy):
    """
    Conservative threshold-based strategy with short-memory defensive mode.

    It tracks an internal state S derived from recent opponent cooperation,
    uses round-dependent thresholds, and defaults to defection under unstable
    or insufficiently cooperative conditions.
    """

    def __init__(self, game_description: CollectiveRiskDescription):
        self.game_description = game_description
        self.m = int(game_description.m)
        self.r = int(game_description.n_rounds)
        self.S = 0
        self.defect_next_two = 0

    def _clamp_S(self, value: int) -> int:
        return max(-6, min(6, value))

    def _threshold_for_round(self, t: int) -> int:
        if t <= 2:
            return 3
        if t >= self.r - 1:
            return 1
        return 2

    def __call__(self, history: PlayerHistory) -> Action:
        t = history.round_number + 1
        if t == 1:
            self.S = 0
            self.defect_next_two = 0
            return Action.D
        x_prev = int(history.opponent_cooperators[-1])
        if x_prev >= self.m:
            self.S += 2
            self.defect_next_two = 0
        elif x_prev == self.m - 1:
            self.S += 1
        else:
            self.S -= 2
            if x_prev < self.m - 1:
                if history.round_number >= 2:
                    x_prev2 = int(history.opponent_cooperators[-2])
                    if x_prev2 < self.m - 1:
                        self.defect_next_two = 2
        self.S = self._clamp_S(self.S)
        if self.defect_next_two > 0:
            self.defect_next_two -= 1
            return Action.D
        if t == 2:
            if x_prev >= self.m - 1 and self.S >= 3:
                return Action.C
            return Action.D
        if t == self.r:
            if x_prev >= self.m - 1 and self.S >= 1:
                return Action.C
            return Action.D
        recent_ok = x_prev >= self.m - 1
        at_least_one_of_last_two_ok = recent_ok
        if history.round_number >= 2:
            x_prev2 = int(history.opponent_cooperators[-2])
            at_least_one_of_last_two_ok = recent_ok or x_prev2 >= self.m - 1
        last_three = history.opponent_cooperators[-3:] if history.round_number >= 3 else history.opponent_cooperators
        cooperative_rounds_last_three = 0
        for val in last_three:
            if int(val) >= self.m - 1:
                cooperative_rounds_last_three += 1
        if cooperative_rounds_last_three < 2 and (not at_least_one_of_last_two_ok):
            return Action.D
        threshold = self._threshold_for_round(t)
        if self.S >= threshold and recent_ok and at_least_one_of_last_two_ok:
            return Action.C
        return Action.D

class Strategy_INDIVIDUALISTIC_4(BaseStrategy):
    """
    Narrow, threshold-sensitive cooperation strategy for the Collective Risk Dilemma.

    Default action is D. Cooperate only when recent opponent cooperation is
    persistently near the pivotal threshold and not declining.
    """

    def __init__(self, game_description: CollectiveRiskDescription):
        self.game_description = game_description
        self.n_players = int(game_description.n_players)
        self.n_rounds = int(game_description.n_rounds)
        self.m = int(game_description.m)
        self.k = float(game_description.k)
        self.window_size = 3

    def __call__(self, history: PlayerHistory) -> Action:
        t = int(history.round_number)
        if t == 0:
            return Action.D
        opp = history.opponent_cooperators
        last_o = int(opp[-1])
        start = max(0, t - self.window_size)
        recent = opp[start:t]
        recent_list = [int(x) for x in recent.tolist()] if len(recent) > 0 else []
        threshold_or_above = self.m - 1
        below_threshold = self.m - 2
        above_count = 0
        below_count = 0
        for o in recent_list:
            if o >= threshold_or_above:
                above_count += 1
            if o <= below_threshold:
                below_count += 1
        support_score = above_count - below_count
        non_falling = True
        if t >= 2:
            prev_o = int(opp[-2])
            non_falling = last_o >= prev_o
        if self.m == 2:
            pivotal = 1
            if last_o != pivotal:
                return Action.D
            pivot_count = 0
            for o in recent_list:
                if o == pivotal:
                    pivot_count += 1
            if pivot_count >= 2 and non_falling:
                return Action.C
            return Action.D
        if self.m == self.n_players - 1:
            pivotal = self.n_players - 2
            if last_o != pivotal:
                return Action.D
            pivot_count = 0
            for o in recent_list:
                if o == pivotal:
                    pivot_count += 1
            if pivot_count >= 2 and non_falling:
                return Action.C
            return Action.D
        if last_o == self.m - 1 and support_score >= 2 and non_falling:
            return Action.C
        return Action.D