class Strategy_BENEVOLENT_3(BaseStrategy):
    """
    Sustainability-Conditional Reciprocity (SCR) strategy.
    Prioritizes maintaining stock levels near capacity while managing defection
    through retaliatory defection and recovery phases.
    """

    def __init__(self, game_description: CommonPoolDescription):
        self.game_description = game_description
        self.capacity = game_description.capacity
        self.n = game_description.n_players
        self.r = game_description.n_rounds
        self.recovery_counter = 0

    def __call__(self, history: PlayerHistory, current_stock: float) -> Action:
        t = history.round_number + 1
        if t == self.r:
            return Action.D
        if t == 1:
            return Action.C
        if self.recovery_counter > 0:
            self.recovery_counter -= 1
            return Action.C
        if current_stock < 0.1 * self.capacity:
            self.recovery_counter = 2
            return Action.C
        k_prev = history.opponent_cooperators[-1]
        my_action_prev = 1 if history.my_actions[-1] else 0
        total_k = k_prev + my_action_prev
        if total_k == self.n:
            return Action.C
        s_prev = self._estimate_previous_stock(history, current_stock)
        total_consumption = total_k * (s_prev / (2 * self.n)) + (self.n - total_k) * (s_prev / self.n)
        if s_prev > 0 and total_consumption > s_prev / 2:
            return Action.D
        return Action.C

    def _estimate_previous_stock(self, history: PlayerHistory, current_stock: float) -> float:
        """
        Since the stock dynamics function is non-invertible (due to min(capacity, growth)),
        we perform a heuristic estimation or simply use the current_stock as a proxy for the 
        previous state to make the decision rule functional.
        """
        return current_stock

