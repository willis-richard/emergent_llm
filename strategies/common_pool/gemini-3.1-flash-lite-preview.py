class Strategy_ALTRUISTIC_2(BaseStrategy):
    """
    Tit-for-Tat Sustainability (TFTS) Strategy:
    Prioritizes maintaining the common pool stock at capacity for long-term optimization.
    - Cooperates by default in the first round.
    - Monitors stock levels: enters 'Recovery Mode' (Cooperate) if stock is critically low (< 20% capacity).
    - Aggregates behavior: If stock is shrinking due to defectors (previous round), may retaliate (Defect).
    - End-game: Switches to Defect if stock is healthy (> 50% capacity) in the final 3 rounds.
    """

    def __init__(self, game_description: CommonPoolDescription):
        self.game_description = game_description

    def _calculate_stock_from_previous(self, prev_stock: float, my_prev_action: Action, prev_opp_coop: int) -> float:
        """Helper to calculate what the stock would have been at the end of the previous round."""
        n = self.game_description.n_players
        cap = self.game_description.capacity
        my_consumption = prev_stock / (2 * n) if my_prev_action == Action.C else prev_stock / n
        num_opp_defectors = n - 1 - prev_opp_coop
        opp_c_consumption = prev_opp_coop * (prev_stock / (2 * n))
        opp_d_consumption = num_opp_defectors * (prev_stock / n)
        total_consumption = my_consumption + opp_c_consumption + opp_d_consumption
        s_remaining = max(0.0, prev_stock - total_consumption)
        growth = 2.0 * s_remaining * (1.0 - s_remaining / cap)
        new_stock = min(cap, s_remaining + growth)
        return new_stock

    def __call__(self, history: PlayerHistory, current_stock: float) -> Action:
        if history.round_number == 0:
            return Action.C
        if history.round_number >= self.game_description.n_rounds - 3:
            if current_stock > 0.5 * self.game_description.capacity:
                return Action.D
            else:
                return Action.C
        if current_stock < 0.2 * self.game_description.capacity:
            return Action.C
        if not hasattr(self, '_last_stock'):
            self._last_stock = float(self.game_description.capacity)
        prev_stock = self._last_stock
        prev_my_action = history.my_actions[-1]
        prev_opp_coop = history.opponent_cooperators[-1]
        observed_new_stock = self._calculate_stock_from_previous(prev_stock, prev_my_action, prev_opp_coop)
        self._last_stock = current_stock
        if observed_new_stock >= prev_stock:
            return Action.C
        prev_defectors = self.game_description.n_players - 1 - prev_opp_coop
        if prev_defectors > 0:
            return Action.D
        return Action.C

