class Strategy_ALTRUISTIC_2(BaseStrategy):
    """
    Adaptive commons-preservation strategy.

    - Starts with cooperation and cooperates on the final round.
    - Uses a weighted estimate of recent opponent cooperation.
    - Cooperates when stock is healthy or cooperation history is strong.
    - Defects sparingly when stock is strained and recent cooperation is weak,
      while avoiding consecutive defections unless conditions remain poor.
    """

    def __init__(self, game_description: CommonPoolDescription):
        self.game_description = game_description
        self.n_players = int(game_description.n_players)
        self.n_rounds = int(game_description.n_rounds)
        self.capacity = float(game_description.capacity)
        self.n_opponents = max(self.n_players - 1, 0)

    def __call__(self, history: PlayerHistory, current_stock: float) -> Action:
        round_number = int(history.round_number)
        if round_number == 0 or round_number >= self.n_rounds - 1:
            return Action.C
        cap = self.capacity if self.capacity > 0 else 1.0
        x = float(current_stock) / cap
        full_coop = self.n_opponents
        if self.n_opponents <= 0:
            c_prev = 0
            c_prev2 = 0
            rho = 1.0
        else:
            c_prev = int(history.opponent_cooperators[-1]) if round_number >= 1 else full_coop
            c_prev2 = int(history.opponent_cooperators[-2]) if round_number >= 2 else full_coop
            c_prev = max(0, min(c_prev, self.n_opponents))
            c_prev2 = max(0, min(c_prev2, self.n_opponents))
            rho = (0.7 * c_prev + 0.3 * c_prev2) / float(self.n_opponents)
        last_my_action = bool(history.my_actions[-1]) if round_number >= 1 else True
        last_two_my_d = False
        if round_number >= 2:
            last_two_my_d = not bool(history.my_actions[-1]) and (not bool(history.my_actions[-2]))
        unanimous_last_round = c_prev == self.n_opponents if self.n_opponents > 0 else True
        heavy_defection_last_round = c_prev < self.n_opponents / 2.0 if self.n_opponents > 0 else False
        if x >= 0.9:
            return Action.C
        if unanimous_last_round and (not last_two_my_d):
            return Action.C
        if x >= 0.7:
            return Action.C
        low_stock_weak_coop = x < 0.4 and rho < 0.65
        mid_stock_weak_coop = 0.4 <= x < 0.7 and rho < 0.5
        if heavy_defection_last_round:
            if last_my_action:
                if low_stock_weak_coop or mid_stock_weak_coop:
                    return Action.D
                return Action.C
            return Action.C
        if not last_my_action:
            if low_stock_weak_coop:
                return Action.D
            return Action.C
        if mid_stock_weak_coop or low_stock_weak_coop:
            return Action.D
        return Action.C

