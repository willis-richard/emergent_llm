        if round_number == 0:
            return Action.C
        if current_stock <= 0:
            return Action.C
        remaining_rounds = self.n_rounds - round_number
        if remaining_rounds <= 0:
            return Action.C
        if remaining_rounds == 1:
            return Action.C
        opp_hist = history.opponent_cooperators
        if opp_hist is None or len(opp_hist) == 0:
            return Action.C
        last_opp_coop = int(opp_hist[-1])
