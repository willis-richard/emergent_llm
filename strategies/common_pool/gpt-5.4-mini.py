class Strategy_SELFINTERESTED_3(BaseStrategy):
    """
    Adaptive common-pool strategy that estimates opponents' cooperation with
    exponential smoothing and cooperates only when needed to keep the stock in
    the high-recovery region; otherwise it defects.
    """

    def __init__(self, game_description: CommonPoolDescription):
        self.game_description = game_description
        self.memory_weight = 0.5
        self.default_opponent_coop_rate = 1.0

    def __call__(self, history: PlayerHistory, current_stock: float) -> Action:
        n_players = int(self.game_description.n_players)
        n_rounds = int(self.game_description.n_rounds)
        capacity = float(self.game_description.capacity)
        n_opponents = max(n_players - 1, 0)
        half_capacity = capacity / 2.0
        p_est = self.default_opponent_coop_rate
        rounds_played = int(history.round_number)
        if n_opponents > 0 and rounds_played > 0:
            opp_counts = history.opponent_cooperators
            for k_t in opp_counts:
                coop_rate = float(k_t) / float(n_opponents)
                p_est = self.memory_weight * p_est + (1.0 - self.memory_weight) * coop_rate
            p_est = min(max(p_est, 0.0), 1.0)
        else:
            p_est = 1.0 if n_opponents > 0 else 0.0
        if rounds_played >= max(n_rounds - 1, 0):
            return Action.D
        khat = float(n_opponents) * p_est
        remaining_if_c = float(current_stock) * (khat + 1.0) / (2.0 * float(n_players))
        remaining_if_d = float(current_stock) * khat / (2.0 * float(n_players))
        if remaining_if_d >= half_capacity:
            return Action.D
        if remaining_if_c >= half_capacity:
            return Action.C
        return Action.D

class Strategy_SELFINTERESTED_4(BaseStrategy):
    """
    Adaptive common-pool strategy with smoothed opponent-cooperation tracking.

    Rules:
    - First round: cooperate.
    - Final round: defect.
    - Very low stock: cooperate until stock recovers.
    - Low estimated cooperation: defect.
    - Very high estimated cooperation with healthy stock: defect.
    - Otherwise cooperate.
    """

    def __init__(self, game_description: CommonPoolDescription):
        self.game_description = game_description
        self.n_players = int(game_description.n_players)
        self.n_rounds = int(game_description.n_rounds)
        self.capacity = float(game_description.capacity)
        self.eps = 1e-12
        self.qbar_initial = 1.0

    def _smoothed_cooperation(self, history: PlayerHistory) -> float:
        """
        Compute \x08ar{q}_{t-1} from the full observed history using:
            qbar_t = 2/3 * qbar_{t-1} + 1/3 * q_t
        starting from qbar_0 = 1.
        """
        qbar = self.qbar_initial
        n_opponents = max(self.n_players - 1, 1)
        for cooperators in history.opponent_cooperators:
            q_t = float(cooperators) / float(n_opponents)
            if q_t < 0.0:
                q_t = 0.0
            elif q_t > 1.0:
                q_t = 1.0
            qbar = 2.0 / 3.0 * qbar + 1.0 / 3.0 * q_t
        return qbar

    def __call__(self, history: PlayerHistory, current_stock: float) -> Action:
        round_number = int(history.round_number)
        current_stock = float(current_stock)
        if round_number >= self.n_rounds - 1:
            return Action.D
        if round_number == 0:
            return Action.C
        capacity = self.capacity if self.capacity > self.eps else self.eps
        x_t = current_stock / capacity
        qbar_prev = self._smoothed_cooperation(history)
        if x_t <= 0.4:
            return Action.C
        if qbar_prev < 0.5:
            return Action.D
        if qbar_prev >= 0.75 and x_t >= 0.65:
            return Action.D
        return Action.C

class Strategy_OPPORTUNISTIC_1(BaseStrategy):
    """
    Cautious probe-and-harvest strategy for the Common Pool Resource Game.
    Cooperates initially, protects the pool under stress, and defects in the final round.
    """

    def __init__(self, game_description: CommonPoolDescription):
        self.game_description = game_description
        self._low_coop_cooldown = 0
        self._shock_protection = False
        self._last_stock = None
        self._last_round_number = -1

    def __call__(self, history: PlayerHistory, current_stock: float) -> Action:
        n_players = int(self.game_description.n_players)
        n_opponents = max(1, n_players - 1)
        capacity = float(self.game_description.capacity)
        round_number = int(history.round_number)
        final_round_index = max(0, int(self.game_description.n_rounds) - 1)
        if round_number == 0:
            self._last_stock = float(current_stock)
            self._last_round_number = round_number
            return Action.C
        x = float(current_stock) / capacity if capacity > 0 else 0.0
        prev_opponent_cooperators = int(history.opponent_cooperators[-1]) if len(history.opponent_cooperators) > 0 else 0
        g_prev = prev_opponent_cooperators / n_opponents if n_opponents > 0 else 0.0
        g_prev2 = None
        if len(history.opponent_cooperators) >= 2:
            g_prev2 = int(history.opponent_cooperators[-2]) / n_opponents if n_opponents > 0 else 0.0
        delta = None
        if self._last_stock is not None:
            delta = float(current_stock) - float(self._last_stock)
        if g_prev <= 1.0 / 3.0:
            self._low_coop_cooldown = max(self._low_coop_cooldown, 2)
        if delta is not None and delta < -(capacity / 6.0):
            self._shock_protection = True
        if self._shock_protection and x > 2.0 / 3.0:
            self._shock_protection = False
        if round_number >= final_round_index:
            self._low_coop_cooldown = max(0, self._low_coop_cooldown - 1)
            self._last_stock = float(current_stock)
            self._last_round_number = round_number
            return Action.D
        if x <= 1.0 / 3.0:
            self._low_coop_cooldown = max(0, self._low_coop_cooldown - 1)
            self._last_stock = float(current_stock)
            self._last_round_number = round_number
            return Action.C
        if self._shock_protection and x <= 2.0 / 3.0:
            self._low_coop_cooldown = max(0, self._low_coop_cooldown - 1)
            self._last_stock = float(current_stock)
            self._last_round_number = round_number
            return Action.C
        if self._low_coop_cooldown > 0:
            self._low_coop_cooldown -= 1
            self._last_stock = float(current_stock)
            self._last_round_number = round_number
            return Action.C
        defect = False
        if x >= 0.5 and g_prev >= 2.0 / 3.0 and (delta is not None and delta >= 0.0):
            defect = True
        elif x >= 0.7 and g_prev >= 0.5 and (g_prev2 is not None) and (g_prev2 >= 0.5):
            defect = True
        self._last_stock = float(current_stock)
        self._last_round_number = round_number
        return Action.D if defect else Action.C

