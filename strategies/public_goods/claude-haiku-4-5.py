class Strategy_SELFINTERESTED_3(BaseStrategy):
    """
    Adaptive Threshold-Based Exploitation strategy for N-Player Public Goods Game.
    
    Divides game into two phases:
    - Phase 1 (first 2/3 of rounds): Unconditional cooperation
    - Phase 2 (remaining rounds): Cooperate if opponent cooperation exceeds threshold,
      otherwise defect. Threshold is (k*n)/(k+n).
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        self.n = game_description.n_players
        self.r = game_description.n_rounds
        self.k = game_description.k
        self.t_learn = math.floor(2 * self.r / 3)
        self.threshold = self.k * self.n / (self.k + self.n)
        self.phase2_decision = None
        phase2_length = self.r - self.t_learn
        self.reevaluation_round = None
        if phase2_length > 5:
            self.reevaluation_round = self.t_learn + math.floor(phase2_length / 2)

    def __call__(self, history: PlayerHistory) -> Action:
        current_round = history.round_number
        if current_round < self.t_learn:
            return Action.C
        if self.phase2_decision is None:
            phase1_cooperators = history.opponent_cooperators[:self.t_learn]
            m = np.mean(phase1_cooperators) if len(phase1_cooperators) > 0 else 0.0
            if m >= self.threshold:
                self.phase2_decision = Action.C
            else:
                self.phase2_decision = Action.D
        if self.reevaluation_round is not None and current_round == self.reevaluation_round:
            recent_cooperators = history.opponent_cooperators[self.t_learn:current_round + 1]
            m_recent = np.mean(recent_cooperators) if len(recent_cooperators) > 0 else 0.0
            if m_recent < self.threshold:
                self.phase2_decision = Action.D
        return self.phase2_decision

class Strategy_SELFINTERESTED_4(BaseStrategy):
    """
    Adaptive Threshold Cooperation Strategy for N-Player Public Goods Game.
    
    Cooperates unconditionally in round 1, uses dynamic threshold-based conditional
    cooperation in intermediate rounds (with 2-round moving average smoothing),
    and defects unconditionally in the final round. Switches to permanent defection
    if opponents achieve zero cooperation.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        self.permanent_defection = False

    def __call__(self, history: PlayerHistory) -> Action:
        n = self.game_description.n_players
        k = self.game_description.k
        r = self.game_description.n_rounds
        current_round_number = history.round_number
        current_round_1indexed = current_round_number + 1
        if current_round_1indexed == 1:
            return Action.C
        if current_round_1indexed == r:
            return Action.D
        if self.permanent_defection:
            return Action.D
        n_opponents = n - 1
        if current_round_1indexed == 2:
            p = history.opponent_cooperators[-1] / n_opponents if n_opponents > 0 else 0.0
        else:
            recent_coop = history.opponent_cooperators[-1] + history.opponent_cooperators[-2]
            p = recent_coop / (2.0 * n_opponents) if n_opponents > 0 else 0.0
        if p == 0.0:
            self.permanent_defection = True
            return Action.D
        threshold = 1.0 - k / n + current_round_1indexed / r * (1.0 - k / n) / 2.0
        if p >= threshold:
            return Action.C
        else:
            return Action.D

