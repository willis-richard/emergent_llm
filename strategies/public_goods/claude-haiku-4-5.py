        n_rounds = self.game_description.n_rounds
        if current_round == 0:
            return Action.D
        if current_round == n_rounds - 1:
            return Action.D
        prev_opponent_cooperators = history.opponent_cooperators[current_round - 1]
        prev_my_action = history.my_actions[current_round - 1]
        total_cooperators_prev = prev_opponent_cooperators + int(prev_my_action)
        if total_cooperators_prev >= self.breakeven_threshold:
            return Action.C
        else:
            return Action.D

