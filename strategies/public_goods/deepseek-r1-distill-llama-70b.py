class Strategy_OPPORTUNISTIC_32(BaseStrategy):
    """
    Opportunistic Strategy for the N-Player Public Goods Game.

    This strategy starts by cooperating in the first round, defects in the last round,
    and adapts based on the average cooperation rate from previous rounds. It uses a dynamic
    threshold and a punishment mechanism for sustained low cooperation.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description

    def __call__(self, state: GameState, history: None | PlayerHistory) -> Action:
        if state.round_number == 0:
            return Action.C
        elif state.round_number == self.game_description.n_rounds - 1:
            return Action.D
        else:
            n = self.game_description.n_players
            threshold = self.game_description.k / (n + 1)
            cooperator_rates = []
            for s in range(0, state.round_number):
                my_action = history.my_actions[s]
                opponents_actions = history.opponent_actions[s]
                my_coop = 1 if my_action else 0
                opp_coop = opponents_actions.sum()
                sum_coop = my_coop + opp_coop
                rate = sum_coop / n
                cooperator_rates.append(rate)
            avg_c = sum(cooperator_rates) / len(cooperator_rates) if cooperator_rates else 0.0
            if avg_c > threshold:
                action = Action.C
            else:
                action = Action.D
            if len(cooperator_rates) >= 2:
                last = cooperator_rates[-1]
                second_last = cooperator_rates[-2]
                if last < threshold and second_last < threshold:
                    action = Action.D
            return action

