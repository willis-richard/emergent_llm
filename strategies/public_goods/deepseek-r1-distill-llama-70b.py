    """
    This strategy, Adaptive Conditional Cooperation, aims to balance cooperation and self-interest.
    It starts with 50% cooperation, adjusts based on others' previous actions, applies a decay factor,
    and periodically explores by cooperating to test responsiveness.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        self.n_players = game_description.n_players
        self.n_rounds = game_description.n_rounds
        self.k = game_description.k
        self.alpha = 0.1
        self.exploration_prob = 0.05
        self.coop_prob = 0.5

    def __call__(self, state: GameState, history: None | PlayerHistory) -> Action:
        if state.round_number == self.n_rounds - 1:
            return Action.D
        if state.round_number == 0:
            if self.n_rounds == 1:
                return Action.D
            if random.random() < 0.5:
                return Action.C
            else:
                return Action.D
        else:
            prev_round = state.round_number - 1
            my_c = history.my_actions[prev_round]
            opponent_actions = history.opponent_actions[prev_round, :]
            sum_c = int(my_c) + np.sum(opponent_actions)
            avg_c = sum_c / self.n_players
            new_c = (1 - self.alpha) * self.coop_prob + self.alpha * avg_c
            new_c = max(0, min(1, new_c))
            self.coop_prob = new_c
            if random.random() < self.exploration_prob:
                return Action.C
            if random.random() < self.coop_prob:
                return Action.C
            else:
                return Action.D

