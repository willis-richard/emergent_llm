class Strategy_AGGRESSIVE_13(BaseStrategy):

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description

    def __call__(self, state: GameState, history: None | PlayerHistory) -> Action:
        if state.round_number == 0:
            return Action.C
        elif state.round_number == self.game_description.n_rounds - 1:
            prev_coop_rate = self._calculate_coop_rate(history)
            return Action.C if prev_coop_rate >= 0.5 else Action.D
        else:
            n_players = self.game_description.n_players
            my_payoff = history.my_payoffs[-1]
            opponent_payoffs = history.opponent_payoffs[-1]
            sum_opponent_payoffs = np.sum(opponent_payoffs)
            avg_payoff = (my_payoff + sum_opponent_payoffs) / n_players
            prev_my_coop = history.my_actions[-1]
            prev_my_count = 1 if prev_my_coop else 0
            opponent_actions = history.opponent_actions[-1]
            opponent_coops = np.sum(opponent_actions)
            total_coop = prev_my_count + opponent_coops
            cooperation_rate = total_coop / n_players
            defect_prob = 0.5 * (1 - cooperation_rate)
            if my_payoff < avg_payoff:
                defect_prob += 0.5
            opponent_defections = np.sum(np.logical_not(opponent_actions))
            if opponent_defections > 0:
                defect_prob += 0.3
            if total_coop == n_players:
                defect_prob += 0.2
            defect_prob = max(0.0, min(1.0, defect_prob))
            if random.random() < defect_prob:
                return Action.D
            else:
                return Action.C

    def _calculate_coop_rate(self, history: PlayerHistory) -> float:
        my_last_action = history.my_actions[-1]
        opponent_last_actions = history.opponent_actions[-1]
        my_coop = 1 if my_last_action else 0
        opponent_coops = np.sum(opponent_last_actions)
        total_coop = my_coop + opponent_coops
        return total_coop / self.game_description.n_players

