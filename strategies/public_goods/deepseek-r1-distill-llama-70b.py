        my_prev_action = history.my_actions[prev_round]
        my_prev_payoff = history.my_payoffs[prev_round]
        others_actions = history.opponent_actions[prev_round, :]
        others_payoffs = history.opponent_payoffs[prev_round, :]
        sum_c = 0.0
        sum_d = 0.0
        m = 0
        d_count = 0
        if my_prev_action:
            sum_c += my_prev_payoff
            m += 1
        else:
            sum_d += my_prev_payoff
            d_count += 1
        for i in range(len(others_actions)):
            if others_actions[i]:
                sum_c += others_payoffs[i]
                m += 1
            else:
                sum_d += others_payoffs[i]
                d_count += 1
        if m == 0:
            return Action.C
        else:
            pi_d_avg = sum_d / d_count if d_count > 0 else 0.0
            pi_c_avg = sum_c / m
            return Action.C if pi_c_avg >= pi_d_avg else Action.D

