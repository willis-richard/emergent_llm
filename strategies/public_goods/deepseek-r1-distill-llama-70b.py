        opp_last_actions = history.opponent_actions[t_prev, :]
        m_prev = my_last_action + np.sum(opp_last_actions)
        self.recent_coops.append(m_prev)
        if len(self.recent_coops) > 3:
            self.recent_coops = self.recent_coops[-3:]
        if not self.recent_coops:
            trend = 'stable'
            avg = 0.0
        else:
            avg = sum(self.recent_coops) / len(self.recent_coops)
            if avg > 0.8 * m_prev:
                trend = 'positive'
            elif avg < 0.2 * m_prev:
                trend = 'negative'
            else:
                trend = 'stable'
