class Strategy_AGGRESSIVE_9(BaseStrategy):
    """
    Grim Trigger with Forgiveness Strategy.

    - First Round: Defect (D)
    - Subsequent Rounds: Cooperate if past cooperation was beneficial, otherwise Defect.
    - Forgiveness Mechanism: Revert to cooperation if recent history shows sufficient cooperation.
    - Last Round: Defect to maximize final payoff.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        self.n = game_description.n_players
        self.r = game_description.n_rounds
        self.k = game_description.k

    def __call__(self, state: GameState, history: None | PlayerHistory) -> Action:
        if state.round_number == 0:
            return Action.D
        if state.round_number == self.r - 1:
            return Action.D
        if history is not None:
            last_my_action = history.my_actions[-1]
            last_opponent_actions = history.opponent_actions[-1]
            C_last = last_my_action + np.sum(last_opponent_actions)
            threshold = self.k / self.n * C_last
            if threshold >= 1:
                return Action.C
            recent_rounds = len(history.my_actions)
            start = max(0, recent_rounds - 3)
            recent_my = history.my_actions[start:]
            recent_opp = history.opponent_actions[start:]
            sum_my = np.sum(recent_my) if recent_my.size > 0 else 0
            sum_opp = np.sum(recent_opp) if recent_opp.size > 0 else 0
            sum_C_recent = sum_my + sum_opp
            if sum_C_recent >= self.n * 0.5:
                return Action.C
            return Action.D
        return Action.D

