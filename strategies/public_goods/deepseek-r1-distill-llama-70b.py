        if current_round <= self.exploration_rounds:
            if current_round % 2 == 1:
                return Action.D
            else:
                return Action.C
        decay = self.decay
        threshold = self.threshold
        punishment_trigger = self.punishment_trigger
        decayed_sum = 0.0
        for rn in range(current_round):
            my_c = 1.0 if history.my_actions[rn] else 0.0
            opponent_c = np.sum(history.opponent_actions[rn, :])
            total_c = my_c + opponent_c
