        t = history.round_number
        n = self.game_description.n_players
        r = self.game_description.n_rounds
        n_opponents = n - 1
        threshold = math.floor(n_opponents / 2)
        if t == 0:
            return Action.C
        if t == r - 1:
            return Action.D
        if r > 5 and t == r - 2:
            return Action.D
        if self.in_recovery_mode:
            last_opp_c = history.opponent_cooperators[-1]
            if last_opp_c < threshold:
                self.failed_reintegrations += 1
                if self.failed_reintegrations >= 2:
                    self.collapse = True
            self.in_recovery_mode = False
        if self.collapse:
            return Action.D
        if self.in_punishment_mode:
            self.in_punishment_mode = False
            self.in_recovery_mode = True
            return Action.C
        last_opp_c = history.opponent_cooperators[-1]
        if last_opp_c >= threshold:
            return Action.C
        else:
            self.in_punishment_mode = True
            return Action.D

