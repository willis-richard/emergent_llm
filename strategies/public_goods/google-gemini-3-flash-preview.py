class Strategy_INDIVIDUALISTIC_6(BaseStrategy):
    """
    Conditional Cooperation with Gradual Sanctions.
    Initializes with cooperation, terminates with defection.
    Uses a majority threshold (n/2) for intermediate cooperation, 
    monitors trends for warnings, probes for recovery every 3 rounds if defecting, 
    and applies a history-dependent endgame strategy.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.n = game_description.n_players
        self.r = game_description.n_rounds
        self.k = game_description.k
        self.threshold = math.ceil(self.n / 2)
        self.rounds_since_low_coop = 0

    def __call__(self, history: PlayerHistory) -> Action:
        t = history.round_number
        n_players = self.n
        n_rounds = self.r
        if t == 0:
            return Action.C
        if t == n_rounds - 1:
            return Action.D
        prev_round_N = int(history.opponent_cooperators[-1]) + int(history.my_actions[-1])
        if t == n_rounds - 2:
            prev_total_cooperators = history.opponent_cooperators + history.my_actions.astype(int)
            avg_coop_rate = np.mean(prev_total_cooperators) / n_players
            if avg_coop_rate < 0.4:
                return Action.D
            stable_rounds = np.sum(prev_total_cooperators >= n_players - 1)
            if stable_rounds > t / 2:
                return Action.C
        if prev_round_N == n_players:
            return Action.C
        if prev_round_N >= self.threshold:
            if t >= 3:
                n_t1 = prev_round_N
                n_t2 = int(history.opponent_cooperators[-2]) + int(history.my_actions[-2])
                n_t3 = int(history.opponent_cooperators[-3]) + int(history.my_actions[-3])
                if n_t1 < n_t2 < n_t3:
                    return Action.D
            return Action.C
        low_coop_indices = np.where(history.opponent_cooperators + history.my_actions.astype(int) < self.threshold)[0]
        if low_coop_indices.size > 0:
            last_low_t = low_coop_indices[-1]
            if history.my_actions[-1] == True:
                n_t2 = int(history.opponent_cooperators[-2]) + int(history.my_actions[-2]) if t >= 2 else 0
                if prev_round_N <= n_t2:
                    return Action.D
            rounds_since_last_c = 0
            for i in range(t - 1, -1, -1):
                if history.my_actions[i] == False:
                    rounds_since_last_c += 1
                else:
                    break
            if rounds_since_last_c >= 2:
                return Action.C
        return Action.D

class Strategy_INDIVIDUALISTIC_7(BaseStrategy):
    """
    Conditional Cooperation with Threshold Maintenance (CCCTM).
    - Round 1: Signal cooperation.
    - Rounds 2 to (r-1): Cooperate if a critical mass of contributors is met.
    - Responds to exploitation with temporary defection and forgiveness.
    - Switches to permanent defection if threshold failure persists or long-term average is poor.
    - Round r: Always defect.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.n = game_description.n_players
        self.r = game_description.n_rounds
        self.k = game_description.k
        self.threshold = math.ceil(self.n * (1.0 / self.k)) + 1
        self.permanent_defect = False
        self.punishment_streak = 0

    def __call__(self, history: PlayerHistory) -> Action:
        t = history.round_number
        if t == self.r - 1:
            return Action.D
        if t == 0:
            return Action.C
        prev_me_coop = 1 if history.my_actions[-1] == 1 else 0
        m_prev = int(history.opponent_cooperators[-1]) + prev_me_coop
        if t >= 3:
            m_list = []
            for i in range(1, 4):
                me_c = 1 if history.my_actions[-i] == 1 else 0
                m_list.append(history.opponent_cooperators[-i] + me_c)
            avg_m = np.mean(m_list)
            if avg_m < self.n / self.k:
                self.permanent_defect = True
        if self.permanent_defect:
            return Action.D
        if m_prev >= self.threshold:
            self.punishment_streak = 0
            return Action.C
        else:
            if history.my_actions[-1] == 0:
                self.punishment_streak += 1
            if self.punishment_streak >= 2:
                if self.n >= 10 and self.r - t > 5:
                    self.punishment_streak = 0
                    return Action.C
                else:
                    self.permanent_defect = True
                    return Action.D
            if history.my_actions[-1] == 0:
                return Action.C
            return Action.D

class Strategy_INDIVIDUALISTIC_8(BaseStrategy):
    """
    Conditional Threshold Reciprocity (CTR).
    
    Cooperates initially and then follows a threshold based on the k-factor.
    Implements a punishment/recovery cycle if cooperation fails, tests group
    resilience with a 5% defection probe if perfect cooperation persists for 
    three rounds, and always defects in the final round.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        self.n = game_description.n_players
        self.r = game_description.n_rounds
        self.k = game_description.k
        self.threshold = math.floor((self.n - 1) / self.k) + 1

    def __call__(self, history: PlayerHistory) -> Action:
        t = history.round_number
        if t == 0:
            return Action.C
        if t == self.r - 1:
            return Action.D
        m_prev = history.opponent_cooperators[-1]
        n_opponents = self.n - 1
        my_prev_move = history.my_actions[-1]
        if t >= 3:
            perfect_streak = True
            for i in range(1, 4):
                if history.opponent_cooperators[-i] < n_opponents:
                    perfect_streak = False
                    break
            if perfect_streak:
                if random.random() < 0.05:
                    return Action.D
        if m_prev >= self.threshold:
            return Action.C
        elif my_prev_move == Action.D.value or my_prev_move == False:
            if n_opponents > 0:
                p_recover = m_prev / n_opponents
                if random.random() < p_recover:
                    return Action.C
            return Action.D
        else:
            return Action.D