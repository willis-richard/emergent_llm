        if t >= 2:
            c_prev_prev = history.opponent_cooperators[-2] + (1 if history.my_actions[-2] else 0)
            if c_prev <= 1 and c_prev_prev <= 1:
                self.permanent_defect = True
        if self.permanent_defect:
            pass
        if t == r - 1:
            return Action.D
        if t == r - 2:
            if n > 5:
                return Action.D
            else:
                if t >= 3:
                    all_c = True
                    for i in range(1, 4):
                        total_at_i = history.opponent_cooperators[-i] + (1 if history.my_actions[-i] else 0)
                        if total_at_i < n:
                            all_c = False
                            break
                    if all_c:
                        return Action.C
                return Action.D
        if t >= 2:
            c_prev_prev = history.opponent_cooperators[-2] + (1 if history.my_actions[-2] else 0)
            if c_prev < 0.5 * c_prev_prev:
                return Action.D
