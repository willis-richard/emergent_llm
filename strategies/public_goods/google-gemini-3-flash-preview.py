        if t == 0:
            return Action.C
        if self.r <= 3:
            return Action.D
        last_my_action = history.my_actions[-1]
        last_opp_c = history.opponent_cooperators[-1]
        last_total_c = last_opp_c + (1 if last_my_action else 0)
