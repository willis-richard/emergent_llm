        n = self.n
        r = self.r
        k = self.k
        if t == 0:
            return Action.C
        last_opponent_c = history.opponent_cooperators[-1]
        c_rate = last_opponent_c / (n - 1) if n > 1 else 0
        base_threshold = (n - k) / (k * (n - 1)) if k * (n - 1) != 0 else 1.0
        if k / n > 0.8:
            threshold = base_threshold - 0.1
        else:
            threshold = base_threshold
        if t == r - 1:
            return Action.D
        if t == r - 2:
            if t >= 1:
                prev_prev_opp_c = history.opponent_cooperators[-1]
                if prev_prev_opp_c == n - 1:
                    return Action.C
            return Action.D
