class Strategy_ALTRUISTIC_2(BaseStrategy):
    """
    Generous Adaptive Cooperation for repeated n-player Public Goods Game.
    Starts cooperatively, rewards any observed cooperation, and only defects
    after persistent complete defection by others.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        self.trust = 0
        self.last_seen_round = 0

    def __call__(self, history: PlayerHistory) -> Action:
        n_rounds = self.game_description.n_rounds
        n_players = self.game_description.n_players
        opponents = max(n_players - 1, 1)
        if history.round_number == 0:
            self.trust = 0
            self.last_seen_round = 0
            return Action.C
        if history.round_number > self.last_seen_round:
            m_prev = int(history.opponent_cooperators[-1])
            q_prev = m_prev / opponents
            if q_prev == 1.0:
                self.trust = min(self.trust + 2, 3)
            elif q_prev >= 0.5:
                self.trust = min(self.trust + 1, 3)
            elif q_prev > 0.0:
                self.trust = max(self.trust - 1, -3)
            else:
                self.trust = max(self.trust - 2, -3)
            self.last_seen_round = history.round_number
        else:
            m_prev = int(history.opponent_cooperators[-1])
            q_prev = m_prev / opponents
        current_round = history.round_number + 1
        late_threshold = n_rounds - math.ceil(n_rounds / 3)
        if current_round == n_rounds:
            if q_prev == 0.0 and self.trust <= -2:
                return Action.D
            return Action.C
        if self.trust <= -2 and q_prev == 0.0:
            return Action.D
        if current_round > late_threshold and q_prev >= 0.25:
            return Action.C
        return Action.C

