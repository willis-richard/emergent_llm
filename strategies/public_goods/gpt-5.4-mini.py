class Strategy_INDIVIDUALISTIC_2(BaseStrategy):
    """
    Skeptical public-goods strategy with conditional cooperation, short-memory
    stability checks, and a two-round forced-defection lockout after severe
    cooperation drops.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        self._processed_rounds = 0
        self._p_history = []
        self._lockout_remaining = 0

    def _p_from_count(self, coop_count: int) -> float:
        denom = self.game_description.n_players - 1
        if denom <= 0:
            return 0.0
        return float(coop_count) / float(denom)

    def _sync_history(self, history: PlayerHistory) -> None:
        """
        Process any newly observed rounds since the last call.
        """
        total_rounds = history.round_number
        if total_rounds <= self._processed_rounds:
            return
        for r in range(self._processed_rounds, total_rounds):
            coop_count = int(history.opponent_cooperators[r]) if r < len(history.opponent_cooperators) else 0
            p = self._p_from_count(coop_count)
            self._p_history.append(p)
            if p < 1.0 / 6.0:
                self._lockout_remaining = 2
        self._processed_rounds = total_rounds

    def __call__(self, history: PlayerHistory) -> Action:
        self._sync_history(history)
        if history.round_number == 0:
            return Action.D
        if history.round_number >= self.game_description.n_rounds - 1:
            return Action.D
        if self._lockout_remaining > 0:
            self._lockout_remaining -= 1
            return Action.D
        p_last = self._p_history[-1] if len(self._p_history) >= 1 else 0.0
        last_three = self._p_history[-3:] if len(self._p_history) >= 3 else self._p_history[:]
        last_two = self._p_history[-2:] if len(self._p_history) >= 2 else self._p_history[:]
        recent_avg = float(sum(last_three)) / float(len(last_three)) if len(last_three) > 0 else 0.0
        recent_floor = min(last_two) if len(last_two) > 0 else 0.0
        if p_last >= 0.5 and recent_avg >= 2.0 / 3.0 and (recent_floor >= 0.5):
            return Action.C
        return Action.D

class Strategy_INDIVIDUALISTIC_3(BaseStrategy):
    """
    Conditional late-stage cooperation strategy for the N-player Public Goods Game.

    - Defect in the first and final rounds.
    - In intermediate rounds, cooperate only after two consecutive rounds in which
      all opponents cooperated.
    - Otherwise defect.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        self.n_players = int(game_description.n_players)
        self.n_rounds = int(game_description.n_rounds)
        self.k = float(game_description.k)
        self.state = Action.D
        self._cooperation_eligible = False

    def __call__(self, history: PlayerHistory) -> Action:
        round_number = int(history.round_number)
        if round_number == 0:
            self.state = Action.D
            self._cooperation_eligible = False
            return Action.D
        if round_number >= self.n_rounds - 1:
            self.state = Action.D
            self._cooperation_eligible = False
            return Action.D
        n_opponents = self.n_players - 1
        if round_number < 2:
            self.state = Action.D
            self._cooperation_eligible = False
            return Action.D
        last_two = history.opponent_cooperators[-2:]
        all_opponents_cooperated_twice = True
        for x in last_two:
            if int(x) != n_opponents:
                all_opponents_cooperated_twice = False
                break
        if all_opponents_cooperated_twice:
            self.state = Action.C
            self._cooperation_eligible = True
            return Action.C
        self.state = Action.D
        self._cooperation_eligible = False
        return Action.D

class Strategy_INDIVIDUALISTIC_4(BaseStrategy):
    """
    Threshold-based public goods strategy:
    - Defect in round 1 and in the final two rounds
    - Enter cooperation only after sustained high cooperation by opponents
    - Remain cooperative while recent cooperation stays above a recovery threshold
    - After any zero-cooperation round, defect for the next two rounds
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        n = max(2, int(getattr(game_description, 'n_players', 2)))
        k = float(getattr(game_description, 'k', 1.0))
        self.n_opponents = n - 1
        if k > 1.0:
            self.threshold = math.ceil(self.n_opponents * (1.0 - 1.0 / k))
        else:
            self.threshold = self.n_opponents
        self.threshold = max(0, int(self.threshold))
        self.recovery_threshold = max(0, self.threshold - 1)
        self.stable = False
        self.zero_cooldown_remaining = 0
        self._last_processed_round = 0

    def __call__(self, history: PlayerHistory) -> Action:
        t = int(history.round_number)
        r = int(getattr(self.game_description, 'n_rounds', 0))
        if t == 0:
            self.stable = False
            self.zero_cooldown_remaining = 0
            self._last_processed_round = 0
            return Action.D
        if self._last_processed_round < t:
            q_last = int(history.opponent_cooperators[-1]) if len(history.opponent_cooperators) > 0 else 0
            if q_last == 0:
                self.zero_cooldown_remaining = 2
            self._last_processed_round = t
        if r > 0 and t >= r - 1:
            if self.zero_cooldown_remaining > 0:
                self.zero_cooldown_remaining = max(0, self.zero_cooldown_remaining - 1)
            self.stable = False
            return Action.D
        if self.zero_cooldown_remaining > 0:
            self.zero_cooldown_remaining -= 1
            return Action.D
        if len(history.opponent_cooperators) >= 2:
            if int(history.opponent_cooperators[-1]) == self.n_opponents and int(history.opponent_cooperators[-2]) == self.n_opponents:
                self.stable = True
                return Action.C
        q_prev = int(history.opponent_cooperators[-1]) if len(history.opponent_cooperators) > 0 else 0
        if self.stable:
            if q_prev >= self.recovery_threshold:
                return Action.C
            self.stable = False
            return Action.D
        if t >= 2 and len(history.opponent_cooperators) >= 2:
            q_prev2 = int(history.opponent_cooperators[-2])
            if q_prev >= self.threshold and q_prev2 >= self.threshold:
                self.stable = True
                return Action.C
        return Action.D