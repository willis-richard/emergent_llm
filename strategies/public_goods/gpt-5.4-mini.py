        r = self.game_description.n_rounds
        x = history.opponent_cooperators
        if history.round_number == 0:
            return Action.D
        if len(x) > 0 and np.any(x == 0):
            self._permanent_defect = True
            return Action.D
        if self._permanent_defect:
            return Action.D
        stable = False
        if len(x) >= 2:
            stable = bool(x[-1] >= n - 2 and x[-2] >= n - 2)
        if history.round_number >= max(0, r - 2):
            if stable and len(x) >= 2:
                if x[-1] >= n - 2 and x[-1] <= n - 1 and (x[-2] >= n - 2) and (x[-2] <= n - 1):
                    return Action.C
