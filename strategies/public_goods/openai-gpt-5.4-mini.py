class Strategy_INDIVIDUALISTIC_5(BaseStrategy):
    """
    Cautious, hysteretic public-goods strategy with smoothed opponent-cooperation
    estimates and adaptive cooperation thresholds.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        self.alpha = 0.5
        self.theta0 = 0.55
        self.theta_slope = 0.15
        self.delta = 0.1
        self.epsilon = 0.0
        self.theta_adjust = 0.0
        self.theta_adjust_step = 0.02
        self.theta_min = 0.5
        self.processed_rounds = 0
        self.smooth_coop = 0.0
        self.last_m = 0.0
        self.last_m_prev = 0.0
        self.coop_payoff_sum = 0.0
        self.coop_payoff_count = 0
        self.defect_payoff_sum = 0.0
        self.defect_payoff_count = 0
        self.last_state = Action.D
        self.recent_window = 3

    def _theta(self, round_number_1based: int) -> float:
        r = max(1, int(self.game_description.n_rounds))
        if r <= 1:
            base = self.theta0
        else:
            base = self.theta0 + self.theta_slope * (round_number_1based - 1) / (r - 1)
        theta = base + self.theta_adjust
        return max(self.theta_min, min(1.0, theta))

    def _coop_payoff_not_worse(self) -> bool:
        if self.coop_payoff_count == 0 or self.defect_payoff_count == 0:
            return False
        avg_c = self.coop_payoff_sum / max(1, self.coop_payoff_count)
        avg_d = self.defect_payoff_sum / max(1, self.defect_payoff_count)
        return avg_c >= avg_d - self.epsilon

    def _opponent_cooperation_stable(self, history: PlayerHistory) -> bool:
        t = history.round_number
        if t <= 1:
            return False
        recent = history.opponent_cooperators
        n_opp = max(1, self.game_description.n_players - 1)
        start = max(0, t - self.recent_window)
        rates = []
        for idx in range(start, t):
            rates.append(float(recent[idx]) / n_opp)
        if len(rates) < 2:
            return False
        for i in range(1, len(rates)):
            if rates[i] + 1e-12 < rates[i - 1] - self.delta:
                return False
        return True

    def _update_from_new_history(self, history: PlayerHistory) -> None:
        n = self.game_description.n_players
        n_opp = max(1, n - 1)
        for idx in range(self.processed_rounds, history.round_number):
            x = int(history.opponent_cooperators[idx])
            m = float(x) / n_opp
            self.last_m_prev = self.last_m
            self.last_m = m
            self.smooth_coop = self.alpha * m + (1.0 - self.alpha) * self.smooth_coop
            my_action = bool(history.my_actions[idx])
            my_payoff = float(history.my_payoffs[idx])
            if my_action:
                self.coop_payoff_sum += my_payoff
                self.coop_payoff_count += 1
            else:
                self.defect_payoff_sum += my_payoff
                self.defect_payoff_count += 1
            self.last_state = Action.C if my_action else Action.D
            if self.coop_payoff_count + self.defect_payoff_count >= 4:
                if self.coop_payoff_count > 0 and self.defect_payoff_count > 0:
                    avg_c = self.coop_payoff_sum / self.coop_payoff_count
                    avg_d = self.defect_payoff_sum / self.defect_payoff_count
                    if avg_c <= avg_d:
                        self.theta_adjust = min(0.2, self.theta_adjust + self.theta_adjust_step)
                    elif self._opponent_cooperation_stable(history):
                        self.theta_adjust = max(self.theta_min - self.theta0, self.theta_adjust - self.theta_adjust_step)
        self.processed_rounds = history.round_number

    def __call__(self, history: PlayerHistory) -> Action:
        if history.round_number > self.processed_rounds:
            self._update_from_new_history(history)
        if history.round_number == 0:
            self.last_state = Action.D
            return Action.D
        current_round = history.round_number + 1
        n_rounds = self.game_description.n_rounds
        if current_round >= n_rounds:
            self.last_state = Action.D
            return Action.D
        theta = self._theta(current_round)
        if self.last_state == Action.C:
            if self.smooth_coop < theta - self.delta:
                self.last_state = Action.D
                return Action.D
            self.last_state = Action.C
            return Action.C
        stable_enough = self.smooth_coop >= theta
        not_collapsing = self.last_m >= theta - self.delta
        payoff_ok = self._coop_payoff_not_worse()
        if stable_enough and not_collapsing and payoff_ok:
            self.last_state = Action.C
            return Action.C
        self.last_state = Action.D
        return Action.D

class Strategy_INDIVIDUALISTIC_6(BaseStrategy):
    """
    Adaptive cooperation strategy for the repeated N-player Public Goods Game.

    - Starts with cooperation.
    - Maintains an exponentially smoothed estimate of opponent cooperation.
    - Uses a clipped personal threshold to decide whether to cooperate.
    - Adds a small persistence bias when recent cooperation has been stable.
    - Defects in the final round unless the previous round showed sufficient cooperation.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        self.n_players = int(game_description.n_players)
        self.n_rounds = int(game_description.n_rounds)
        self.k = float(game_description.k)
        self.n_opponents = max(1, self.n_players - 1)
        self.q_low = 0.1
        self.lambda_standard = 0.35
        self.lambda_early = 0.5
        self.q0 = 0.5
        self.thresh_min = 0.15
        self.thresh_max = 0.85

    def _clip(self, x: float, lo: float, hi: float) -> float:
        return max(lo, min(hi, x))

    def _q_from_history(self, history: PlayerHistory, idx: int) -> float:
        if idx < 0 or idx >= history.round_number:
            return 0.0
        m_t = int(history.opponent_cooperators[idx])
        if self.n_opponents <= 0:
            return 0.0
        if m_t <= 0:
            return 0.0
        if m_t >= self.n_opponents:
            return 1.0
        return m_t / self.n_opponents

    def _estimate_Q(self, history: PlayerHistory, upto_idx: int) -> float:
        q_values = []
        for t in range(upto_idx + 1):
            q_values.append(self._q_from_history(history, t))
        Q = self.q0
        for t, q_t in enumerate(q_values, start=1):
            lam = self.lambda_early if t in (2, 3) else self.lambda_standard
            Q = (1.0 - lam) * Q + lam * q_t
        return Q

    def _persistence(self, history: PlayerHistory, current_q: float, current_Q: float) -> int:
        s = 0
        for idx in range(history.round_number - 1, -1, -1):
            q_idx = self._q_from_history(history, idx)
            if abs(q_idx - current_Q) <= self.q_low:
                s += 1
            else:
                break
        return s

    def __call__(self, history: PlayerHistory) -> Action:
        t = history.round_number
        if t == 0:
            return Action.C
        if t == self.n_rounds - 1:
            prev_q = self._q_from_history(history, t - 1)
            return Action.C if prev_q >= 0.5 else Action.D
        current_q = self._q_from_history(history, t - 1)
        current_Q = self._estimate_Q(history, t - 1)
        tau = self._clip(0.5 + 0.4 * (0.5 - current_Q), self.thresh_min, self.thresh_max)
        s_t = self._persistence(history, current_q, current_Q)
        current_action = Action.C if bool(history.my_actions[-1]) else Action.D
        if s_t >= 3:
            if current_action == Action.D and current_q >= tau - 0.05:
                return Action.C
            if current_action == Action.C and current_q <= tau + 0.05:
                return Action.C
        if current_q > tau:
            return Action.C
        if current_q < tau:
            return Action.D
        if history.round_number >= 2:
            prev_q = self._q_from_history(history, t - 2)
            if prev_q == current_q:
                return current_action
        return current_action if current_action == Action.C else Action.D

class Strategy_INDIVIDUALISTIC_7(BaseStrategy):
    """
    Conservative threshold-based public goods strategy:
    - Defect initially and during a short probing phase.
    - Cooperate only when recent observed cooperation is consistently high.
    - Enforce a short cooldown after two consecutive below-threshold observations.
    - Always defect in the final round.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        self.n_players = int(game_description.n_players)
        self.n_rounds = int(game_description.n_rounds)
        self.k = float(game_description.k)
        self.observation_window = max(1, min(3, self.n_rounds - 1))
        self.threshold = max(1, math.ceil((self.n_players - 1) / self.k)) if self.k > 0 else self.n_players
        self.cooldown_remaining = 0

    def __call__(self, history: PlayerHistory) -> Action:
        t = history.round_number
        if t == 0:
            return Action.D
        if t >= self.n_rounds - 1:
            return Action.D
        if len(history.opponent_cooperators) >= 2:
            if history.opponent_cooperators[-1] < self.threshold and history.opponent_cooperators[-2] < self.threshold:
                self.cooldown_remaining = 2
        if self.cooldown_remaining > 0:
            self.cooldown_remaining -= 1
            return Action.D
        if t <= self.observation_window:
            return Action.D
        window = self.observation_window
        recent = history.opponent_cooperators[-window:] if len(history.opponent_cooperators) >= window else history.opponent_cooperators
        avg = float(np.mean(recent)) if len(recent) > 0 else 0.0
        if len(history.opponent_cooperators) >= 2:
            last_ok = history.opponent_cooperators[-1] >= self.threshold
            prev_ok = history.opponent_cooperators[-2] >= self.threshold
        else:
            last_ok = False
            prev_ok = False
        if avg >= self.threshold and last_ok and prev_ok:
            return Action.C
        return Action.D

class Strategy_INDIVIDUALISTIC_8(BaseStrategy):
    """
    Conservative public-goods strategy:
    - starts with defection,
    - explores briefly,
    - cooperates only in stable majority-cooperation environments,
    - punishes low cooperation,
    - defects in the endgame unless cooperation is clearly stable.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        self.s = 0
        self.mode = 'explore'
        self.punish_until_round = 0
        self._last_processed_round = 0

    def _majority_threshold(self) -> int:
        return math.ceil((self.game_description.n_players - 1) / 2)

    def _punish_threshold(self) -> int:
        return math.ceil((self.game_description.n_players - 1) / 3)

    def _clamp_s(self) -> None:
        self.s = max(-3, min(3, self.s))

    def _process_observation(self, round_index: int, my_action: bool, x: int) -> None:
        majority_thr = self._majority_threshold()
        punish_thr = self._punish_threshold()
        if round_index + 1 < self.punish_until_round and x >= majority_thr:
            self.punish_until_round = round_index + 1
            self.s = 0
        if my_action and x >= majority_thr:
            self.s += 1
        elif not my_action and x <= majority_thr - 1:
            self.s -= 1
        self._clamp_s()
        if x < punish_thr:
            self.punish_until_round = max(self.punish_until_round, round_index + 3)

    def __call__(self, history: PlayerHistory) -> Action:
        t = history.round_number
        n = self.game_description.n_players
        r = self.game_description.n_rounds
        if t == 0:
            self._last_processed_round = 0
            return Action.D
        start = self._last_processed_round
        end = t
        for idx in range(start, end):
            my_action = bool(history.my_actions[idx])
            x = int(history.opponent_cooperators[idx])
            self._process_observation(idx, my_action, x)
        self._last_processed_round = t
        majority_thr = self._majority_threshold()
        punish_thr = self._punish_threshold()
        prev_x = int(history.opponent_cooperators[-1])
        prev_majority = prev_x >= majority_thr
        in_punish = t < self.punish_until_round
        if t >= r - 2:
            if prev_majority and self.s >= 2 and (not in_punish):
                return Action.C
            return Action.D
        if in_punish:
            return Action.D
        if t <= min(3, r) and self.mode == 'explore':
            if t == 1:
                return Action.C if prev_majority else Action.D
            if t == 2:
                return Action.C if bool(history.my_actions[-1]) else Action.D
        recent_start = max(0, t - 3)
        recent_window = history.opponent_cooperators[recent_start:t]
        if len(recent_window) == 0 or n - 1 <= 0:
            m = 0.0
        else:
            m = float(np.mean(recent_window / (n - 1)))
        if t >= 3:
            self.mode = 'exploit'
        if m >= 0.5 and self.s >= 0 and prev_majority:
            return Action.C
        return Action.D