class Strategy_ALTRUISTIC_2(BaseStrategy):
    """
    Lead with cooperation, then mirror a cautious public-goods response:
    cooperate whenever any opponent cooperated recently, defect only after a
    full-round collapse, and use a short cooldown/test period before returning
    to normal cooperation.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        self._state = 'normal'
        self._cooldown = 0
        self._last_round_number = -1

    def __call__(self, history: PlayerHistory) -> Action:
        n_rounds = self.game_description.n_rounds
        n_players = self.game_description.n_players
        if history is None or history.round_number == 0:
            self._last_round_number = 0 if history is not None else self._last_round_number
            return Action.C
        if history.round_number <= self._last_round_number:
            try:
                last_opp = history.opponent_cooperators[-1]
                q = last_opp / max(1, n_players - 1)
            except Exception:
                q = None
            if history.round_number == n_rounds:
                if self._state == 'cautious' and q == 0:
                    return Action.D
                return Action.C
            if self._cooldown > 0:
                return Action.C
            if q == 0:
                return Action.D
            return Action.C
        q = None
        try:
            if history.opponent_cooperators is not None and len(history.opponent_cooperators) > 0:
                last_opp = int(history.opponent_cooperators[-1])
                denom = n_players - 1
                q = last_opp / denom if denom > 0 else 0.0
        except Exception:
            q = None
        if q is None:
            self._last_round_number = history.round_number
            return Action.C
        if history.round_number == n_rounds:
            self._last_round_number = history.round_number
            if self._state == 'cautious' and q == 0:
                return Action.D
            return Action.C
        action = Action.C
        if self._cooldown > 0:
            action = Action.C
            self._cooldown -= 1
        elif q == 0:
            action = Action.D
            self._state = 'cautious'
            self._cooldown = 1
        elif q > 0 and q < 1 / 3:
            action = Action.C
        else:
            action = Action.C
            self._state = 'normal'
        if self._state == 'cautious':
            if q > 0:
                self._state = 'normal'
                self._cooldown = 0
            elif q == 0 and self._cooldown == 0:
                self._cooldown = 1
        self._last_round_number = history.round_number
        return action

