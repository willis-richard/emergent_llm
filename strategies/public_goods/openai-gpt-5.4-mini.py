class Strategy_ALTRUISTIC_5(BaseStrategy):
    """
    Adaptive cooperative strategy for the repeated N-player Public Goods Game.

    Starts with cooperation, forgives brief slumps, defects only after sustained
    weak cooperation, and immediately returns to cooperation after a strong
    cooperative signal from opponents.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        self.tau_high = 0.6
        self.tau_low = 0.3
        self.tau_recover = 0.5

    def __call__(self, history: PlayerHistory) -> Action:
        t = history.round_number
        if t == 0:
            return Action.C
        opp = history.opponent_cooperators
        n_players = self.game_description.n_players
        n_opponents = max(1, n_players - 1)
        s_last = float(opp[-1]) / n_opponents
        if bool(history.my_actions[-1]) is False and s_last >= self.tau_high:
            return Action.C
        if len(opp) > 0:
            avg_coop = float(np.mean(opp)) / n_opponents
        else:
            avg_coop = 0.0
        if s_last >= self.tau_recover:
            return Action.C
        if avg_coop >= self.tau_low:
            if len(opp) >= 2:
                s_prev = float(opp[-2]) / n_opponents
                if not (s_last < self.tau_low and s_prev < self.tau_low):
                    return Action.C
            else:
                return Action.C
        return Action.D

