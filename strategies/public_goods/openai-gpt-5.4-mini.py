class Strategy_COLLECTIVE_6(BaseStrategy):
    """
    Threshold-based coordination strategy for the N-player Public Goods Game.

    - Cooperates initially.
    - Tracks recent opponent cooperation rates.
    - Stays cooperative when recent cooperation is at or above the threshold (k - 1) / k.
    - Defects after sustained drops, with a small periodic probe to test recovery.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        self.n_players = int(game_description.n_players)
        self.n_rounds = int(game_description.n_rounds)
        self.k = float(game_description.k)
        self.threshold = (self.k - 1.0) / self.k if self.k != 0 else 0.0
        self._probe_period = 4

    def _q_values(self, history: PlayerHistory):
        n_opponents = max(self.n_players - 1, 1)
        opp = history.opponent_cooperators
        return [float(x) / float(n_opponents) for x in opp]

    def _recent_average(self, q_values, window_size: int) -> float:
        if not q_values:
            return 0.0
        if window_size <= 0:
            return float(q_values[-1])
        window = q_values[-window_size:]
        return float(sum(window)) / float(len(window))

    def __call__(self, history: PlayerHistory) -> Action:
        t = history.round_number + 1
        if history.round_number == 0:
            return Action.C
        q_values = self._q_values(history)
        last_q = q_values[-1] if q_values else 0.0
        prev_q = q_values[-2] if len(q_values) >= 2 else 0.0
        if t == 2:
            return Action.C if last_q >= 0.5 else Action.D
        recent_window = min(3, len(q_values))
        avg_q = self._recent_average(q_values, recent_window)
        trend = last_q - prev_q if len(q_values) >= 2 else 0.0
        base_cooperate = avg_q >= self.threshold and last_q >= self.threshold and (trend >= -0.25)
        if t >= self.n_rounds:
            return Action.C if avg_q >= self.threshold else Action.D
        if len(q_values) >= 2:
            if q_values[-1] < self.threshold and q_values[-2] < self.threshold:
                if t % self._probe_period == 0:
                    return Action.C
        return Action.C if base_cooperate else Action.D

