class Strategy_COLLECTIVE_8(BaseStrategy):
    """
    Threshold-based public goods strategy:
    start cooperative, remain cooperative while opponent cooperation stays at or above
    tau = 1 - 1/k, switch to defensive after a drop below threshold, and return to
    cooperation only after two consecutive acceptable rounds. Defect on the final round.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        k = float(game_description.k)
        self.tau = 1.0 - 1.0 / k if k != 0 else 1.0
        self.mode = 'cooperative'
        self.acceptable_streak = 0
        self._last_seen_round = -1

    def __call__(self, history: PlayerHistory) -> Action:
        n_rounds = int(self.game_description.n_rounds)
        n_players = int(self.game_description.n_players)
        n_opponents = max(0, n_players - 1)
        current_round = int(history.round_number)
        if current_round == 0:
            self.mode = 'cooperative'
            self.acceptable_streak = 0
            self._last_seen_round = 0
            return Action.C
        if self._last_seen_round != current_round - 1:
            self.mode = 'cooperative'
            self.acceptable_streak = 0
            for t in range(1, current_round):
                opp_coops = int(history.opponent_cooperators[t - 1])
                rho = opp_coops / n_opponents if n_opponents > 0 else 0.0
                if t == n_rounds - 1:
                    self.mode = 'cooperative'
                    self.acceptable_streak = 0
                    break
                if self.mode == 'cooperative':
                    if rho >= self.tau:
                        self.acceptable_streak = 0
                    else:
                        self.mode = 'defensive'
                        self.acceptable_streak = 0
                elif rho >= self.tau:
                    self.acceptable_streak += 1
                    if self.acceptable_streak >= 2:
                        self.mode = 'cooperative'
                        self.acceptable_streak = 0
                else:
                    self.acceptable_streak = 0
            self._last_seen_round = current_round - 1
        if current_round == n_rounds - 1:
            self.mode = 'cooperative'
            self.acceptable_streak = 0
            self._last_seen_round = current_round
            return Action.D
        prev_opp_coops = int(history.opponent_cooperators[-1])
        rho_prev = prev_opp_coops / n_opponents if n_opponents > 0 else 0.0
        if self.mode == 'cooperative':
            if rho_prev >= self.tau:
                action = Action.C
            else:
                self.mode = 'defensive'
                self.acceptable_streak = 0
                action = Action.D
        else:
            action = Action.D
            if rho_prev >= self.tau:
                self.acceptable_streak += 1
                if self.acceptable_streak >= 2:
                    self.mode = 'cooperative'
                    self.acceptable_streak = 0
            else:
                self.acceptable_streak = 0
        self._last_seen_round = current_round
        return action

class Strategy_PROSOCIAL_1(BaseStrategy):
    """
    Cooperate by default, defect only after two consecutive rounds in which the
    observed number of cooperating opponents is at most floor((n-1)/3), and
    always cooperate in the first and last rounds.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        self._n_opponents = max(0, self.game_description.n_players - 1)
        self._low_threshold = math.floor(self._n_opponents / 3)
        self.low_streak = 0
        self._processed_rounds = 0

    def __call__(self, history: PlayerHistory) -> Action:
        current_rounds = history.round_number
        if current_rounds > self._processed_rounds:
            for idx in range(self._processed_rounds, current_rounds):
                x_t = int(history.opponent_cooperators[idx])
                if x_t <= self._low_threshold:
                    self.low_streak += 1
                else:
                    self.low_streak = 0
            self._processed_rounds = current_rounds
        if history.round_number == 0:
            return Action.C
        next_round = history.round_number + 1
        if next_round == self.game_description.n_rounds:
            return Action.C
        if self.low_streak >= 2:
            return Action.D
        return Action.C

class Strategy_PROSOCIAL_2(BaseStrategy):
    """
    Adaptive Prosocial Threshold Strategy:
    - Start by cooperating.
    - Cooperate when recent opponent cooperation is at least a majority threshold.
    - Defect for one round after sustained low cooperation, unless forgiven by a strong cooperative signal.
    - In the final two rounds, follow the endgame rule based on recent cooperation only.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        self.punishment_remaining = 0
        self.last_seen_round = 0

    def __call__(self, history: PlayerHistory) -> Action:
        n = self.game_description.n_players
        current_round = history.round_number
        if current_round < self.last_seen_round:
            self.punishment_remaining = 0
        self.last_seen_round = current_round
        if current_round == 0:
            return Action.C
        threshold = math.ceil((n - 1) / 2)
        L = min(5, current_round - 1)
        if L > 0:
            recent = history.opponent_cooperators[current_round - L:current_round]
            m_t = float(np.mean(recent)) if len(recent) > 0 else 0.0
        else:
            m_t = 0.0
        if history.opponent_cooperators[-1] >= threshold + 1:
            self.punishment_remaining = 0
        if current_round >= self.game_description.n_rounds - 2:
            return Action.C if m_t >= threshold else Action.D
        if self.punishment_remaining > 0:
            self.punishment_remaining -= 1
            return Action.D
        if m_t >= threshold:
            return Action.C
        self.punishment_remaining = 1
        return Action.D

