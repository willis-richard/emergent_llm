        n_rounds = self.game_description.n_rounds
        t = history.round_number
        if t == 0:
            return Action.C
        opp = history.opponent_cooperators
        last_opp = int(opp[-1])
        h_t = last_opp / n_opponents
        s_t = None
        b_t = 0.0
        d_t = 0
        for idx in range(t):
            h = int(opp[idx]) / n_opponents
            if s_t is None:
                s_t = h
            else:
                s_t = self.alpha * s_t + self.beta * h
            if h > b_t:
                b_t = h
            if h <= self.L:
                d_t += 1
            else:
                d_t = 0
        if s_t is None:
            s_t = h_t
        prev_h = None
        if t >= 2:
            prev_h = int(opp[-2]) / n_opponents
        if t >= n_rounds - 2:
            if h_t >= self.M and (prev_h is not None and prev_h >= self.M):
                return Action.C
            return Action.D
        if t >= n_rounds - 1:
            return Action.D
        if h_t == 0:
            return Action.D
        if d_t >= 2:
            return Action.D
        if t > 2 and s_t < self.L:
            return Action.D
        if h_t < self.M and t > 2:
            return Action.D
        if h_t >= self.H:
            return Action.C
        if s_t >= self.M and h_t >= self.M:
            return Action.C
        if b_t >= self.H and h_t >= self.M:
            return Action.C
        if t <= 2 and h_t >= self.M:
            return Action.C
        if h_t > s_t:
            return Action.C
        if h_t < s_t:
            return Action.D
        return Action.C if h_t >= self.M else Action.D

