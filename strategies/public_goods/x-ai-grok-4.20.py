        n_opponents = self.game_description.n_players - 1
        cur = history.round_number
        if cur == n_rounds - 1:
            return Action.D
        if cur == 0:
            return Action.C
        if np.all(history.opponent_cooperators[:cur] == n_opponents):
            return Action.C
        return Action.D

