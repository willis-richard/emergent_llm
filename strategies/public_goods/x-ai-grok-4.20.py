class Strategy_INDIVIDUALISTIC_4(BaseStrategy):
    """
    Cooperate on the first round and defect on the last round.
    In intermediate rounds, cooperate if and only if all opponents
    have cooperated in every previous round (grim trigger);
    otherwise defect permanently thereafter.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        self.n_rounds = game_description.n_rounds
        self.n_opponents = game_description.n_players - 1

    def __call__(self, history: PlayerHistory) -> Action:
        if history.round_number == self.n_rounds - 1:
            return Action.D
        past_opp_coops = history.opponent_cooperators[:history.round_number]
        if np.all(past_opp_coops == self.n_opponents):
            return Action.C
        return Action.D

class Strategy_INDIVIDUALISTIC_5(BaseStrategy):
    """
    Cooperate first, defect last. For middle rounds, reconstruct previous
    total cooperators and play C exactly when it meets or exceeds m_min
    and no prior round has ever fallen below m_min. Otherwise defect and
    lock into permanent defection. Reset lock (and has_fallen) only after
    two consecutive fully cooperative rounds (total cooperators == n).
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.n = game_description.n_players
        self.r = game_description.n_rounds
        self.k = game_description.k
        self.m_min = math.floor(self.n / self.k) + 1
        self.locked = False
        self.has_fallen = False
        self.consecutive_full = 0

    def __call__(self, history: PlayerHistory) -> Action:
        if history.round_number == 0:
            return Action.C
        if history.round_number == self.r - 1:
            return Action.D
        my_last = history.my_actions[-1]
        opp_last = history.opponent_cooperators[-1]
        total_last = opp_last + (1 if my_last else 0)
        if total_last == self.n:
            self.consecutive_full += 1
        else:
            self.consecutive_full = 0
        if total_last < self.m_min:
            self.has_fallen = True
            self.locked = True
        if self.consecutive_full >= 2:
            self.has_fallen = False
            self.locked = False
        if self.locked:
            return Action.D
        if total_last >= self.m_min and (not self.has_fallen):
            return Action.C
        else:
            self.locked = True
            return Action.D

class Strategy_INDIVIDUALISTIC_6(BaseStrategy):
    """
    Cooperate in round 1. Defect in round r regardless. In rounds 2 to r-1
    cooperate if m >= ceil((n-1)/2) where m is prior round opponent cooperators,
    otherwise defect. Once any m < threshold is observed, defect in all
    subsequent rounds (grim trigger).
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.n_players = game_description.n_players
        self.n_rounds = game_description.n_rounds
        self.threshold = math.ceil((self.n_players - 1) / 2)

    def __call__(self, history: PlayerHistory) -> Action:
        if history.round_number == 0:
            return Action.C
        if history.round_number == self.n_rounds - 1:
            return Action.D
        if any((m < self.threshold for m in history.opponent_cooperators)):
            return Action.D
        return Action.C

class Strategy_INDIVIDUALISTIC_7(BaseStrategy):
    """
    Cooperate in round 1, defect in round r. In rounds 1 < t < r, cooperate if m (opponents cooperating in t-1) >= n - k; otherwise defect. Once m drops below n - k with multiple rounds remaining, permanently defect in all subsequent rounds.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        self.n = game_description.n_players
        self.r = game_description.n_rounds
        self.k = game_description.k
        self.triggered = False

    def __call__(self, history: PlayerHistory) -> Action:
        if history.round_number == 0:
            return Action.C
        rn = history.round_number
        m = int(history.opponent_cooperators[-1])
        threshold = self.n - self.k
        if m < threshold and self.r - rn > 1:
            self.triggered = True
        if self.triggered or rn == self.r - 1:
            return Action.D
        if m >= threshold:
            return Action.C
        return Action.D

class Strategy_INDIVIDUALISTIC_8(BaseStrategy):
    """
    Defect in the final round. In earlier rounds, cooperate only if all
    opponents have cooperated in every previous round (cooperate in round 1).
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        self.n_players = game_description.n_players
        self.n_rounds = game_description.n_rounds
        self.n_opponents = self.n_players - 1

    def __call__(self, history: PlayerHistory) -> Action:
        round_num = history.round_number
        if round_num == self.n_rounds - 1:
            return Action.D
        if round_num == 0 or np.all(history.opponent_cooperators == self.n_opponents):
            return Action.C
        return Action.D