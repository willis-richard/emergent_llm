class Strategy_OPPORTUNISTIC_4(BaseStrategy):
    """
    In round 1 always cooperate. In every round t where 1 < t < r, cooperate exactly
    when both (a) the immediately prior round had at least ceil((n-1)/2) cooperating
    opponents and (b) the running average of cooperating opponents across all prior
    rounds exceeds (n-1)*(k/n); otherwise defect. In round r always defect.
    After any round with exactly 0 cooperating opponents, defect unconditionally for
    the next min(2, r-t) rounds before resuming the above rule. When n=2 the same
    thresholds apply with no special casing.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        self.n = game_description.n_players
        self.r = game_description.n_rounds
        self.k = game_description.k
        self.opp_num = self.n - 1
        self.coop_thresh = math.ceil(self.opp_num / 2)
        self.avg_thresh = self.opp_num * (self.k / self.n)

    def __call__(self, history: PlayerHistory) -> Action:
        rnd = history.round_number
        if rnd == 0:
            return Action.C
        if rnd == self.r - 1:
            return Action.D
        opp_coop = history.opponent_cooperators
        forced = False
        for p in range(rnd):
            if opp_coop[p] == 0:
                t = p + 1
                m = min(2, self.r - t)
                if p + 1 <= rnd <= p + m:
                    forced = True
                    break
        if forced:
            return Action.D
        prev_opp = opp_coop[-1]
        if prev_opp < self.coop_thresh:
            return Action.D
        avg_opp = np.mean(opp_coop)
        if avg_opp > self.avg_thresh:
            return Action.C
        return Action.D

