class Strategy_ALTRUISTIC_1(BaseStrategy):
    """
    Cooperate in round 1. In later rounds compute average opponent
    cooperation rate and cooperate if it exceeds the threshold
    (n - k)/(n - 1) or if any round had full opponent cooperation.
    Use threshold 0.25 in final round. Trigger permanent defection
    if any 3 consecutive rounds had zero opponent cooperations.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description

    def __call__(self, history: PlayerHistory) -> Action:
        n = self.game_description.n_players
        r = self.game_description.n_rounds
        k = self.game_description.k
        if history.round_number == 0:
            return Action.C
        num_opponents = n - 1
        past_rounds = history.round_number
        total_opp_coops = np.sum(history.opponent_cooperators)
        avg = total_opp_coops / (past_rounds * num_opponents)
        coops = history.opponent_cooperators
        triggered_permanent_defect = False
        for i in range(len(coops) - 2):
            if coops[i] == 0 and coops[i + 1] == 0 and (coops[i + 2] == 0):
                triggered_permanent_defect = True
                break
        if triggered_permanent_defect:
            return Action.D
        has_full_prior = np.any(coops == num_opponents)
        is_last = history.round_number == r - 1
        threshold = 0.25 if is_last else (n - k) / (n - 1)
        if avg > threshold or has_full_prior:
            return Action.C
        return Action.D

