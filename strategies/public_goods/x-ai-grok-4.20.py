        if past_rounds > 0 and history.opponent_cooperators[:past_rounds].sum() == 0:
            return Action.D
        past_my = history.my_actions[:past_rounds] * 1
        avg_c = np.mean(past_my + history.opponent_cooperators[:past_rounds])
        if avg_c > self.threshold:
            return Action.C
        return Action.D

