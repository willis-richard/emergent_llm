class Strategy_ALTRUISTIC_7(BaseStrategy):
    """
    Cooperate in round 1. For each subsequent round t, let f be the fraction of all
    prior opponent cooperations. Cooperate if f meets the computed threshold,
    using a stricter last-round threshold. Fall back to time-based rule (cooperate
    first half) if no history or zero opponent cooperation observed.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.n_players = game_description.n_players
        self.n_rounds = game_description.n_rounds
        self.k = game_description.k
        self.threshold = (self.n_players - self.k) / (self.n_players - 1)
        self.last_threshold = (self.n_players - self.k) / (2 * self.n_players)

    def __call__(self, history: PlayerHistory) -> Action:
        t_prior = history.round_number
        if t_prior == 0:
            return Action.C
        total_opp_coop = np.sum(history.opponent_cooperators)
        if total_opp_coop == 0:
            t_current = t_prior + 1
            if t_current <= self.n_rounds / 2:
                return Action.C
            return Action.D
        f = total_opp_coop / (t_prior * (self.n_players - 1))
        t_current = t_prior + 1
        if t_current == self.n_rounds:
            thresh = self.last_threshold
        else:
            thresh = self.threshold
        if f >= thresh:
            return Action.C
        return Action.D

