class Strategy_OPPORTUNISTIC_2(BaseStrategy):
    """
    Cooperate in round 1. For rounds 2 to r-1 compute avg_f as total observed
    cooperating opponents across prior rounds divided by (prior rounds × (n-1)).
    Cooperate if avg_f > 1/k, otherwise defect. Always defect in round r.
    After any two consecutive rounds with zero observed cooperating opponents,
    defect in every remaining round.
    """

    def __init__(self, game_description: PublicGoodsDescription):
        self.game_description = game_description
        self.n = game_description.n_players
        self.r = game_description.n_rounds
        self.k = game_description.k
        self.threshold = 1.0 / self.k
        self.n_opp = self.n - 1

    def __call__(self, history: PlayerHistory) -> Action:
        if history.round_number == 0:
            return Action.C
        if history.round_number == self.r - 1:
            return Action.D
        opp_coop = history.opponent_cooperators
        prior_rounds = history.round_number
        if prior_rounds >= 2:
            if np.any((opp_coop[:-1] == 0) & (opp_coop[1:] == 0)):
                return Action.D
        total_coop_opp = np.sum(opp_coop)
        avg_f = total_coop_opp / (prior_rounds * self.n_opp)
        if avg_f > self.threshold:
            return Action.C
        return Action.D

